# Number List example

a = [10,20,30,40,50]

add = a[0]+a[1]+a[2]+a[3]+a[4]  #it will add all the elements of list 
print(add)

average = add/5         #it will display output in float datatype
print(average)

average = add/5         #type conversion to integer from float
print(int (average))
