#If we want to print the elements from last when there are more elements
#negative indexing list 
#last element will be considered as -1 

arr = ['Football','Cricket','Baseball','Volleball']

print(arr[-1])  #it will print the last element
print(arr[-1][0:4])
print(arr[-1][5:])

arr1 = arr[0][0:4]
print(arr1)
