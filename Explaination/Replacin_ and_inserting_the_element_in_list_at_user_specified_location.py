# Replacing and inserting the element in list at user specified location

country = ['India','Taliban','Russia','Japan','North Korea','Nazi Germany']

country[3] = 'Sweden'   # we are replacing japan with sweden
print(country)

country.insert(1,'Norway')  # we are inserting norway at index 1(location) 
print(country)

country.insert(-1,'Australia') # we are inserting australia at index -1 that is will be inserted by counting from last at 1st index from last
print(country)

country.insert(-15,'Netherland') #  # we are inserting australia at index -1 that is will be inserted by counting from last at 15th index from last
print(country)







