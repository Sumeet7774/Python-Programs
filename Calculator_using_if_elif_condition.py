#Making calculator with the choice of user using if condition

print("1. Sum")
print("2. Sub")
print("3. Mul")
print("4. Div")

choice = input("Enter your choice: ")
choice = int(choice)

if choice == 1 :
    print("Sum is: ",20+20)
elif choice == 2 :
    print("Sub is: ",100-50)
elif choice == 3 : 
    print("Mul is: ",5*5)
elif choice == 4 :
    print("Div is: ",10/5)
else : 
    print("Invalid Choice")
    
    

    
    
    



